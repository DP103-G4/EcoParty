package tw.dp103g4.friendship;

public class FriendShip {
	
	private int idOne;
	private int idTwo;
	
	public FriendShip(int idOne, int idTwo) {
		super();
		this.idOne = idOne;
		this.idTwo = idTwo;
	}
	public int getIdOne() {
		return idOne;
	}
	public void setIdOne(int idOne) {
		this.idOne = idOne;
	}
	public int getIdTwo() {
		return idTwo;
	}
	public void setIdTwo(int idTwo) {
		this.idTwo = idTwo;
	}
	
	

}
